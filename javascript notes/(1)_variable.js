// Variables

// Variables are containers for storing data (storing data values)
// Cannot start with number
// Cannot use the reserve words

/* 
    4 Ways to Declare a JavaScript Variable:
     Using var
     Using let
     Using const
     Using nothing 
*/

// Ex:
var a = 13
let ap = "Deep"
const Hamburger = ["Hi", 23]



/*   var, let, const   */

// var is globally scoped, while let and var are block scoped
// var can be updated and re-declared 
// let can be updated but cannot re-declared
// const can neither be updated nor be re-declared